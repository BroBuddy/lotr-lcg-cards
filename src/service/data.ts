export const findCycleById = (id: number) => {
    return Cycles.find((cycle: any) => cycle.id == id)
}

export const countCardsFromScenario = (scenario: any) => {
    return scenario?.scenarios
        .map((item: any) => {
            return item.quests.length + item.cards.length
        })
        .reduce((sum: number, card: number) => sum + card, 0)
}

export const getListOfCycles = () => {
    return Cycles.map((cycle: any) => {
        return {
            id: cycle.id,
            title: cycle.title,
            scenarios: cycle.scenarios.map((scenario: any) => scenario.title),
        }
    })
}
